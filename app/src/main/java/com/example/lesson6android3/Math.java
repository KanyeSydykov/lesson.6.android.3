package com.example.lesson6android3;

public class Math {

    public int add(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public int umnojenie(int a, int b) {
        return a * b;
    }

    public int delenie(int a, int b) {
        return a / b;
    }
}
